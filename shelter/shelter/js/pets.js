let currentPage = 1;


function oneClickLeft() {
    if (currentPage > 1) {
        currentPage--;
        updateCards(currentPage);
        PageNumber();
    }
}

function twoClickLeft() {
    currentPage = 1;
    updateCards(currentPage);
    PageNumber();
    btnActive();
}

function oneClickRight() {
    if (currentPage < maxPages) {
        currentPage++;
        updateCards(currentPage);
        PageNumber();
        btnActive();
    }
}

function twoClickRight() {
    currentPage = maxPages;
    updateCards(currentPage);
    PageNumber();
    btnActive();
}


function PageNumber(){
  currentPageNumber.innerHTML = currentPage;
}

function btnActive(){
  if (currentPage < maxPages && currentPage > 1) {
    rightBtnAll.classList.remove("active_btn_arrow");
    rightBtn.classList.remove("active_btn_arrow");
    leftBtnALL.classList.remove("active_btn_arrow");
    leftBtn.classList.remove("active_btn_arrow");
  } if (currentPage === maxPages ) {
    rightBtnAll.classList.add("active_btn_arrow");
    rightBtn.classList.add("active_btn_arrow");
    leftBtnALL.classList.remove("active_btn_arrow");
    leftBtn.classList.remove("active_btn_arrow");
  } if(currentPage === 1){
    rightBtnAll.classList.remove("active_btn_arrow");
    rightBtn.classList.remove("active_btn_arrow");
    leftBtnALL.classList.add("active_btn_arrow");
    leftBtn.classList.add("active_btn_arrow");
  }
}
btnActive();




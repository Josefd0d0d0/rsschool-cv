function createArray() {
  let result = [];
  for (let i = 0; i < 6; i++) {
      let subArray = [];
      while (subArray.length < 8) {
          let num = Math.floor(Math.random() * 8);
          if (!subArray.includes(num)) {
              subArray.push(num);
          }
      }
      result.push(subArray);
  }
  return result.flat();
}
const data = createArray();

function splitArray(array, size) {
  console.log(size);
  let result = [];
  for (let i = 0; i < array.length; i += size) {
    result.push(array.slice(i, i + size));
  }
  return result;
}

// Функция для обновления карточек в зависимости от ширины экрана
function updateCards(page) {
  
    const cards = document.querySelectorAll('.pets_container_item');
    // Получаем ширину экрана
    const width = window.innerWidth;
    // Определяем размер части в зависимости от ширины
    let chunkSize;
    let pages;
    if (width <= 320) {
      chunkSize = 3;
      pages = 16;
    } else if (width <= 768) {
      chunkSize = 6;
      pages = 8;
    } else {
      chunkSize = 8;
      pages = 6;
    }
    const chunks = splitArray(data, chunkSize);
    for (let i = 0; i < chunkSize; i++) {
        const card = cards[i];
        const img = card.querySelector('.pets_container_item_img');
        const text = card.querySelector('.pets_container_item_text');
        const button = card.querySelector('.pets_container_item_button');
        img.style.background = `url('${petsMassive[chunks[page-1][i]].img}') 0 0/auto 100% no-repeat`;
        text.innerHTML = petsMassive[chunks[page-1][i]].name;
        button.innerHTML = "Learn more";
    }
}


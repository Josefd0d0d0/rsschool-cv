 function addEventListenersToButtons() {
  const petsButton = document.querySelectorAll('.pets_container_item');
    petsButton.forEach(button => {
        button.addEventListener('click', n => {
            const whoPet = n.currentTarget.querySelector('.pets_container_item_text').textContent;
            const petData = petsFrend[whoPet];
            
            modalWindowImg.style.background = `url('${petData.img}') 0 0/auto 100% no-repeat`;
            modalWindowTitle.textContent = petData.name;
            modalWindowPhrOne.textContent = petData.type;
            modalWindowPhrTwo.textContent = petData.description;
            breedSp.textContent= petData.breed;

            modalWindowListItems[0].textContent = petData.age;
            modalWindowListItems[1].textContent = petData.inoculations;
            modalWindowListItems[2].textContent = petData.diseases;
            modalWindowListItems[3].textContent = petData.parasites;
        
            windowContent.classList.add('window_active');
            openBackground.classList.add('window_background_active');
            body.classList.add("overflow_body");
        });
    });
  };
  addEventListenersToButtons();




  const createCardTemplate = () => {
    const item = document.createElement("div");
    const img = document.createElement("div");
    const pharagraf = document.createElement("p");
    const btn = document.createElement("div");
  
    item.classList.add("pets_container_item");
    img.classList.add("pets_container_item_img");
    pharagraf.classList.add("pets_container_item_text");
    btn.classList.add("pets_container_item_button");
  
    item.appendChild(img);
    item.appendChild(pharagraf);
    item.appendChild(btn);
  
    return { item, img, pharagraf, btn };
  }
  

  const rightBtn = document.querySelector('.button_arrow_right');
  const leftBtn = document.querySelector('.button_arrow_left');
  const carousel = document.querySelector('.pets_container_items');
  const rightItem = document.querySelector('#item_right');
  const leftItem = document.querySelector('#item_left');
  // const active = document.querySelector("#item-active");

  const moveLeft = () => {
    carousel.classList.add("transition-left");
    leftBtn.removeEventListener("click", moveLeft);
    rightBtn.removeEventListener("click", moveRight);
  };

  const moveRight = () => {
    carousel.classList.add("transition-right");
    leftBtn.removeEventListener("click", moveLeft);
    rightBtn.removeEventListener("click", moveRight);
  };

  leftBtn.addEventListener("click", moveLeft);
  rightBtn.addEventListener("click", moveRight);


  carousel.addEventListener("animationend", (animationEvent) => {
    
    let changedItem;
    if (animationEvent.animationName === "move-left") {
      carousel.classList.remove("transition-left");
      changedItem = leftItem;
      document.querySelector("#item_active").innerHTML = leftItem.innerHTML;
    } else {
      carousel.classList.remove("transition-right");
      changedItem = rightItem;
      document.querySelector("#item_active").innerHTML = rightItem.innerHTML;
    }
    changedItem.innerHTML = "";
    let usedIndexes = [];

    let numCards;
    if (window.matchMedia("(max-width: 320px)").matches) {
      numCards = 1;
    } else if (window.matchMedia("(max-width: 768px)").matches) {
      numCards = 2;
    } else {
      numCards = 3;
    }
    for (let i = 0; i < numCards; i++) {
      const Template = createCardTemplate();
      let randomNum;
      do {
        randomNum = Math.floor(Math.random() * 8);
      } while (usedIndexes.includes(randomNum));
      usedIndexes.push(randomNum);
      Template.img.style.background = `url('${petsFriendMassive[randomNum].img}') 0 0/auto 100% no-repeat`;
      Template.pharagraf.innerHTML = petsFriendMassive[randomNum].name;
      Template.btn.innerHTML = "Learn more";
      changedItem.appendChild(Template.item);
    }

    addEventListenersToButtons();
  
  leftBtn.addEventListener("click", moveLeft);
  rightBtn.addEventListener("click", moveRight);
});


